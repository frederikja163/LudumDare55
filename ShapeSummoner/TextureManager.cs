    public static Texture GetFontAtlas()
    {
        Vector4[,] pixels = new Vector4[8 * 16, 8 * 16];

        for (int yc = 0; yc < 16; yc++)
        {
            for (int xc = 0; xc < 16; xc++)
            {
                char c = (char)(xc + yc * 16);
                long bitfield = GetTextureLong(char.ToUpper(c));
                for (int y = 0; y < 8; y++)
                {
                    for (int x = 0; x < 8; x++)
                    {
                        pixels[y + (15 - yc) * 8, x + xc * 8] =
                            (bitfield & 1L << (7 - x + y * 8)) != 0
                                ? Vector4.One
                                : Vector4.Zero;
                    }
                }
            }
        }

        return new Texture(Application.Window, "Font alias", pixels);
    }
